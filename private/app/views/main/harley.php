<p>HARLEY</p>
<p> Harley-Davidson, Inc., H-D, or Harley, is an American motorcycle manufacturer founded in 1903 in Milwaukee, Wisconsin.
 It was one of two major American motorcycle manufacturers to survive the Great Depression, along with Indian.
The company has survived numerous ownership arrangements, subsidiary arrangements, periods of poor economic health and product quality, 
and intense global competition[4] to become one of the world's largest motorcycle manufacturers and an iconic brand widely known for its loyal following. 
There are owner clubs and events worldwide, as well as a company-sponsored, brand-focused museum.

Harley-Davidson is noted for a style of customization that gave rise to the chopper motorcycle style.
 The company traditionally marketed heavyweight, air-cooled cruiser motorcycles with engine displacements greater than 700 cc, 
 but it has broadened its offerings to include more contemporary VRSC (2002) and middle-weight Street (2015) platforms.</p>